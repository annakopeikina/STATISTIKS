# Задача 1: 
# Даны значения величины заработной платы заемщиков банка (zp)
#  и значения их поведенческого кредитного скоринга (ks):
#  zp = [35, 45, 190, 200, 40, 70, 54, 150, 120, 110], 
# ks = [401, 574, 874, 919, 459, 739, 653, 902, 746, 832]. 
# Используя математические операции, посчитать коэффициенты линейной регрессии,
#  приняв за X заработную плату (то есть, zp - признак),
#  а за y - значения скорингового балла (то есть, ks - целевая переменная).
#  Произвести расчет как с использованием intercept, так и без.

import numpy as np

# Дано:
zp = np.array([35, 45, 190, 200, 40, 70, 54, 150, 120, 110])
ks = np.array([401, 574, 874, 919, 459, 739, 653, 902, 746, 832])

# Расчет с интерсептом
n = len(zp)
a_intercept = (n * np.sum(zp * ks) - np.sum(zp) * np.sum(ks)) / (n * np.sum(zp**2) - np.sum(zp)**2)
b_intercept = (np.sum(ks) - a_intercept * np.sum(zp)) / n

# Расчет без интерсепта
a_no_intercept = np.sum(zp * ks) / np.sum(zp**2)

print(f"Коэффициенты линейной регрессии с интерсептом: a = {a_intercept:.4f}, b = {b_intercept:.4f}")
print(f"Коэффициент линейной регрессии без интерсепта: a = {a_no_intercept:.4f}")
