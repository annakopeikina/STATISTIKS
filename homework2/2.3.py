from scipy.stats import binom

n = 144  # количество испытаний (подбрасываний монеты)
k = 70   # количество успехов (выпадений орла)
p = 0.5  # вероятность успеха в одном испытании (симметричная монета)

# Рассчитываем вероятность выпадения орла ровно 70 раз
probability = binom.pmf(k, n, p)
print(f"Вероятность выпадения орла ровно 70 раз из 144 подбрасываний: {probability:.3f}")
