# Дано:
weights = [202, 203, 199, 197, 195, 201, 200, 204, 194, 190]  # ()г. пачек печенья
mean = 200  # Предполагаемое среднее значение генеральной совокупности
n = 10 # Количество семплов в выборке
confidence_level = 0.99  # Доверительная вероятность
# Расчет среднего значения выборки
n = len(weights)
sample_mean = sum(weights) / n
# Расчет стандартного отклонения выборки
sum_sq_diff = sum((x - sample_mean) ** 2 for x in weights)
sample_std = (sum_sq_diff / (n - 1)) ** 0.5
# Расчет критерия Стьюдента t
t = (sample_mean - mean) / (sample_std / (n ** 0.5))
# Нахождение критического значения t для заданной доверительной вероятности и степеней свободы (n - 1)
alpha = 1 - confidence_level
t_critical = 2.8214  # Для n = 10 и доверительной вероятности 99% (двусторонний тест)
if abs(t) > t_critical:
    print("Отвергаем нулевую гипотезу: средний вес пачки печенья не равен 200 г")
else:
    print("Принимаем нулевую гипотезу: средний вес пачки печенья равен 200 г")
