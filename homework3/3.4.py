# A - студент учится на факультете A
# B - студент учится на факультете B
# C - студент учится на факультете C
# Количество студентов на факультетах A и B равно, а количество студентов на факультете C равно сумме студентов на A и B.
# Вероятность сдачи сессии для студента факультета A: (сессия | A)=0.8; P(сессия | A)=0.8,
# Вероятность сдачи сессии для студента факультета B: (сессия | B)=0.7; P(сессия | B)=0.7,
# Вероятность сдачи сессии для студента факультета C: (сессия | C)=0.9; P(сессия | C)=0.9.
#  Найти вероятность того, что студент, сдавший сессию, учится на определенном факультете при условии, что он сдал сессию.
# Используя формулу Байеса:
# Предположим, что количество студентов на факультетах A и B одинаковое и равно P_A = P_B
P_A = 1 / 6  # Предположим произвольное значение для P_A (должно быть менее 1/4, чтобы учесть студентов на C)
# Количество студентов на факультете C в два раза больше, чем количество студентов на факультетах A и B
P_C = 2 * (P_A + P_A)
# Вероятности сдачи сессии для студентов на каждом факультете
P_pass_A = 0.8
P_pass_B = 0.7
P_pass_C = 0.9
# Общая вероятность сдачи сессии
P_session = P_pass_A * P_A + P_pass_B * P_A + P_pass_C * P_C
# Вычисление вероятности учебы на каждом факультете при условии сдачи сессии
P_A_given_pass = (P_pass_A * P_A) / P_session
P_B_given_pass = (P_pass_B * P_A) / P_session
P_C_given_pass = (P_pass_C * P_C) / P_session
print(f"Вероятность учебы на факультете A при условии сдачи сессии: {P_A_given_pass:.2f}")
print(f"Вероятность учебы на факультете B при условии сдачи сессии: {P_B_given_pass:.2f}")
print(f"Вероятность учебы на факультете C при условии сдачи сессии: {P_C_given_pass:.2f}")
