# Вероятности отказа для каждой детали
p1 = 0.1  # Вероятность выхода из строя первой детали
p2 = 0.2  # Вероятность выхода из строя второй детали
p3 = 0.25 # Вероятность выхода из строя третьей детали

# а) Все три детали выйдут из строя
p_all = p1 * p2 * p3
print(f"Вероятность того, что все три детали выйдут из строя: {p_all:.4f}")

# б) Только две детали выйдут из строя
p_two = (p1 * p2 * (1 - p3)) + (p1 * p3 * (1 - p2)) + (p2 * p3 * (1 - p1))
print(f"Вероятность того, что только две детали выйдут из строя: {p_two:.4f}")

# в) Хотя бы одна деталь выйдет из строя
p_at_least_one = 1 - (1 - p1) * (1 - p2) * (1 - p3)
print(f"Вероятность того, что хотя бы одна деталь выйдет из строя: {p_at_least_one:.4f}")

# г) От одной до двух деталей выйдет из строя
p_one_or_two = p_two + (p1 * (1 - p2) * (1 - p3)) + (p2 * (1 - p1) * (1 - p3)) + (p3 * (1 - p1) * (1 - p2))
print(f"Вероятность того, что от одной до двух деталей выйдет из строя: {p_one_or_two:.4f}")
